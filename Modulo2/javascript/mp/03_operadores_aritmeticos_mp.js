console.log("Operadores Aritméticos - Cursos Sacramentales");

participantesBautismo = 10;
participantesConfirmacion = 5;

console.log("Suma de participantes");
totalParticipantes = participantesBautismo + participantesConfirmacion;
console.log("Resultado suma:", totalParticipantes);

console.log("Diferencia de participantes");
diferenciaParticipantes = participantesBautismo - participantesConfirmacion;
console.log("Resultado resta:", diferenciaParticipantes);

console.log("Multiplicación (ejemplo horario x grupos)");
multiplicacion = participantesBautismo * participantesConfirmacion;
console.log("Resultado multiplicacion:", multiplicacion);

console.log("Division de grupos");
division = participantesBautismo / participantesConfirmacion;
console.log("Resultado division:", division);

console.log("Modulo de participantes");
modulo = participantesBautismo % participantesConfirmacion;
console.log("Resultado modulo:", modulo);

console.log("Potencia de participantes");
potencia = participantesBautismo ** participantesConfirmacion;
console.log("Resultado potencia:", potencia);

console.log("Operadores de comparación");
console.log("Igualdad debil ==", "5" == 5);
console.log("Igualdad fuerte ===", "5" === 5);
console.log("Desigualdad debil !=", "5" != 5);
console.log("Desigualdad fuerte !==", "5" !== 5);
console.log("Mayor que >", 10 > 5); 
console.log("Menor que <", 10 < 5);
console.log("Mayor o igual que >=", 10 >= 5);
console.log("Menor o igual que <=", 10 <= 5);

console.log("Operadores lógicos");
console.log("AND &&", "5" <= 5 && "5" >= 5);
console.log("OR ||", "5" <= 5 || "5" >= 6);
console.log("Negacion !", !("5" <= 5 || "5" >= 6));
