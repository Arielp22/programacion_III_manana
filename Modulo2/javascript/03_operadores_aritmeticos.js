console.log("Operadores Aritmeticos");


numero1=10
numero2=5

console.log("suma");
suma=numero1+numero2
console.log("Resultado suma",suma);

console.log("resta");
resta=numero1-numero2
console.log("Resultado resta",resta);

console.log("multiplicacion");
multiplicacion=numero1*numero2
console.log("Resultado multiplicacion",multiplicacion);

console.log("division");
division=numero1/numero2
console.log("Resultado division",division); 

console.log("modulo");
modulo=numero1%numero2
console.log("Resultado modulo",modulo);

console.log("Potencia");
potencia=numero1**numero2
console.log("Resultado potencia",potencia);

console.log("Operadores comparacion");
console.log("Igualdad debil ==", "5" == 5);
console.log("Igualdad fuerte ===", "5" === 5);
console.log("Desigualdad debil !=", "5" != 5);
console.log("Desigualdad fuerte !==", "5" !== 5);
console.log("Mayor que >", 10 > 5); 
console.log("Menor que <", 10 < 5);
console.log("Mayor o igual que >=", 10 >= 5);
console.log("Menor o igual que <=", 10 <= 5);

console.log("Operadores logicos");
console.log("AND &&", "5" <=5 && "5" >=5);
console.log("OR ||", "5" <=5 || "5" >=6);
console.log("Negacion !", !("5" <=5 || "5" >=6));

